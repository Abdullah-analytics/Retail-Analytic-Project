
--Q1  Churn rate: customers who bought in 2009 but not in 2010
WITH CUSTOMERS_2009 AS (
    SELECT DISTINCT CUSTOMERID
    FROM ONLINE_RETAIL_MASTER_TABLE
    WHERE YEARS = 2009
),
CUSTOMERS_2010 AS (
    SELECT DISTINCT CUSTOMERID
    FROM ONLINE_RETAIL_MASTER_TABLE
    WHERE YEARS = 2010
),
CHURNED_CUSTOMERS AS (
    SELECT c2009.CUSTOMERID
    FROM CUSTOMERS_2009 c2009
    LEFT JOIN CUSTOMERS_2010 c2010
        ON c2009.CUSTOMERID = c2010.CUSTOMERID
    WHERE c2010.CUSTOMERID IS NULL
)
SELECT 
    ROUND((COUNT(*)::NUMERIC / (SELECT COUNT(*) FROM CUSTOMERS_2009)) * 100, 2) AS CHURN_RATE_PERCENT
FROM CHURNED_CUSTOMERS;




--Q2 Churn rate: customers who bought in 2009-2010 but did NOT buy in 2011
WITH CUSTOMER_FROM_2009_TO_2010 AS (
    -- All unique customers from 2009 and 2010
    SELECT DISTINCT CUSTOMERID AS UNIQUE_CUSTOMER_FROM_2009_TO_2010
    FROM ONLINE_RETAIL_MASTER_TABLE
    WHERE YEARS IN (2009, 2010)
),

CUSTOMER_FROM_2011 AS (
    -- All unique customers from 2011
    SELECT DISTINCT CUSTOMERID AS UNIQUE_CUSTOMER_FROM_2011
    FROM ONLINE_RETAIL_MASTER_TABLE
    WHERE YEARS = 2011
),

CHURN_CUSTOMERS AS (
    -- Identify customers who bought in 2009-2010 but NOT in 2011
    SELECT 
        UNIQUE_CUSTOMER_FROM_2009_TO_2010,
        UNIQUE_CUSTOMER_FROM_2011
    FROM CUSTOMER_FROM_2009_TO_2010
    LEFT JOIN CUSTOMER_FROM_2011
        ON UNIQUE_CUSTOMER_FROM_2009_TO_2010 = UNIQUE_CUSTOMER_FROM_2011
    WHERE UNIQUE_CUSTOMER_FROM_2011 IS NULL
),

COUNT_CUSTOMERS_FROM_2009_TO_2010 AS (
    -- Total distinct customers in 2009-2010
    SELECT COUNT(DISTINCT CUSTOMERID) AS TOTAL_CUSTOMERS
    FROM ONLINE_RETAIL_MASTER_TABLE
    WHERE YEARS IN (2009, 2010)
),

COUNT_CHURNS AS (
    -- Total churned customers
    SELECT COUNT(*) AS TOTAL_LOST
    FROM CHURN_CUSTOMERS
)

-- Final churn rate percentage
SELECT 
    ROUND((TOTAL_LOST::NUMERIC / TOTAL_CUSTOMERS) * 100, 2) AS CHURNS_RATE_PERCENT
FROM COUNT_CUSTOMERS_FROM_2009_TO_2010, COUNT_CHURNS;
